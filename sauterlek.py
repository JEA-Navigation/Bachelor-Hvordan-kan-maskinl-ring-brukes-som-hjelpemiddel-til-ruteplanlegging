import numpy as np

# Laster inn data fra datafilen og legger disse i variabler
data_array = np.load('data.npy')
data_array = data_array.astype(float)
Y, X, Z = data_array[:,0], data_array[:,1], data_array[:,2]
X = -X

# Setter oppløsninga i kartet lik 150 * 150 ruter
nx = 150
ny = 150

# Setter min og max verdier som brukes i mellomregning
minx = np.min(X)
maxx = np.max(X)
miny = np.min(Y)
maxy = np.max(Y)

# Regner ut dx og setter dy lik. 
dx = ((maxx-minx)/nx)
dy = dx

# Initialiserer matriser som vi senere putter verdier i
pixels = np.zeros((nx+1,ny+1))
nvalues = np.zeros((nx+1,ny+1))

i = 0

dypest = 0

for k in Z:
    # Plassering er ix og iy
    ix = round((X[i]-minx)//dx)
    iy = round((Y[i]-miny)//dy)
    # Finner antall og summen av alle som passer i denne plasseringen.
    nvalues[ix,iy] = nvalues[ix,iy] +1
    pixels[ix,iy] = pixels[ix,iy] + Z[i]
    if Z[i] < dypest:
        dypest = Z[i]
    
    i = i + 1




i = 0
# Omgjør summen til gjennomsnitt
for k in pixels[:,1]:
    j = 0
    for l in pixels[1,:]:
        if nvalues[i,j] > 0:
            pixels[i,j] = round(pixels[i,j]/nvalues[i,j])
            #pixels[i,j] = 1
        else:
            pixels[i,j] = 0
        j = j + 1
    i = i + 1

# Funksjon som setter startposisjonen for HMIen

def convertStartSlutt(Startposisjon,Sluttposisjon):
    #Regner ut startposisjonen
    Startposisjon = Startposisjon.astype(float)
    dataY = Startposisjon[:,0]
    dataX = Startposisjon[:,1]
    dataY = -dataY

    startY = round((dataY[0]-minx)//dx)
    startX = round((dataX[0]-miny)//dy)

    #Regner ut målposisjon
    Sluttposisjon = Sluttposisjon.astype(float)
    dataYs = Sluttposisjon[:,0]
    dataXs = Sluttposisjon[:,1]

    dataYs = -dataYs
    sluttY = round((dataYs[0]-minx)//dx)
    sluttX = round((dataXs[0]-miny)//dy)
    
    #Setter målposisjon
    pixels[sluttY, sluttX] = 10


    return startY, startX

